package helpers

import (
	"github.com/hewiefreeman/GopherGameServer/rooms"
	"github.com/hewiefreeman/GopherGameServer/users"
)

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//   CHANGE User's ROOMS ON ROOM DELETE   /////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

func RoomDeleted(users map[string]rooms.RoomUser){
	for _, u := range users {
		//GET USER
		user, err := users.Get(u.Name());
		if(err == nil){
			users.usersActionChan.Execute(users.ChangeUserRoomName, []interface{}{&user, ""});
		}
	}
}
